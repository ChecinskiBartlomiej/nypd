def morse_encrypt(s):
    tomorse = {
        " " : "_",
        "a" : ".-",
        "b" : "-...",
        "c" : "-.-.",
        "d" : "-..",
        "e" : ".",
        "f" : "..-.",
        "g" : "--.",
        "h" : "....",
        "i" : "..",
        "j" : ".---",
        "k" : "-.-",
        "l" : ".-..",
        "m" : "--",
        "n" : "-.",
        "o" : "---",
        "p" : ".--.",
        "q" : "--.-",
        "r" : ".-.",
        "s" : "...",
        "t" : "-",
        "u" : "..-",
        "w" : ".--",
        "v" : "...-",
        "x" : "-..-",
        "y" : "-.--",
        "z" : "--.."
    }

    word = ""
    for letter in s:
        word += tomorse[letter] + " "
    return word

def morse_decrypt(s):
    toletters = {
        "_" : " ",
        ".-" : "a",
        "-..." : "b",
        "-.-." : "c",
        "-.." : "d",
        "." : "e",
        "..-." : "f",
        "--." : "g",
        "...." : "h",
        ".." : "i",
        ".---" : "j",
        "-.-" : "k",
        ".-.." : "l",
        "--" : "m",
        "-." : "n",
        "---" : "o",
        ".--." : "p",
        "--.-" : "q",
        ".-." : "r",
        "..." : "s",
        "-" : "t",
        "..-" : "u",
        ".--" : "w",
        "...-" : "v",
        "-..-" : "x",
        "-.--" : "y",
        "--.." : "z"
    }

    word = ""
    sign = ""
    for letter in s:
        if(letter !=" "):
            sign += letter
        else:
            word += toletters[sign]
            sign = ""
    return word

